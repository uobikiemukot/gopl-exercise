// visit1 appends to links each link found in n and returns the result.
// loop version
func visit1(links []string, n *html.Node) []string {
	if n.Type == html.ElementNode && n.Data == "a" {
		for _, a := range n.Attr {
			if a.Key == "href" {
				links = append(links, a.Val)
			}
		}
	}
	for c := n.FirstChild; c != nil; c = c.NextSibling {
		links = visit1(links, c)
	}
	return links
}

// visit2 appends to links each link found in n and returns the result.
// recursive version
func visit2(n *html.Node) []string {
	var links []string

	if n == nil {
		return nil
	}

	if n.Type == html.ElementNode && n.Data == "a" {
		for _, a := range n.Attr {
			if a.Key == "href" {
				links = append(links, a.Val)
			}
		}
	}

	links = append(links, visit2(n.NextSibling)...)
	return append(links, visit2(n.FirstChild)...)
}

func main() {
	doc, err := html.Parse(os.Stdin)
	if err != nil {
		fmt.Fprintf(os.Stderr, "findlinks1: %v\n", err)
		os.Exit(1)
	}
	for _, link := range visit2(doc) {
		fmt.Println(link)
	}
}

